package oarlok.resources.json;

public class Rower {
	private String rowerName;
	private String rowerGender;
	private Integer rowerAge;
	private String rowerWeight;
	private String rowerHeight;
	private String rowerProfileImage;
	public String getRowerName() {
		return rowerName;
	}
	public void setRowerName(String rowerName) {
		this.rowerName = rowerName;
	}
	public String getRowerGender() {
		return rowerGender;
	}
	public void setRowerGender(String rowerGender) {
		this.rowerGender = rowerGender;
	}
	public Integer getRowerAge() {
		return rowerAge;
	}
	public void setRowerAge(Integer rowerAge) {
		this.rowerAge = rowerAge;
	}
	public String getRowerWeight() {
		return rowerWeight;
	}
	public void setRowerWeight(String rowerWeight) {
		this.rowerWeight = rowerWeight;
	}
	public String getRowerHeight() {
		return rowerHeight;
	}
	public void setRowerHeight(String rowerHeight) {
		this.rowerHeight = rowerHeight;
	}
	public String getRowerProfileImage() {
		return rowerProfileImage;
	}
	public void setRowerProfileImage(String rowerProfileImage) {
		this.rowerProfileImage = rowerProfileImage;
	}
	
}
